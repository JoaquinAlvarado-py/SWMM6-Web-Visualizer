// inpExporter.js — Generate a SWMM .inp file from the Network model

class InpExporter {

    pad(v, w) { return String(v).padEnd(w); }

    generateInp(net) {
        const units = net.units === 'US' ? 'CFS' : 'LPS';
        const opt = net.options || {};
        const L = [];

        // Determine REPORT_STEP dynamically based on first RAINGAGE interval, if available
        const gages = net.nodes.filter(n => n.type === 'RAINGAGE');
        const needDefaultGage = !gages.length && net.subcatchments.length > 0;
        
        let reportStep = opt.reportStep || '00:15:00';
        if (gages.length > 0 && gages[0].props.interval) {
            let parts = String(gages[0].props.interval).split(':');
            if (parts.length === 2) {
                reportStep = `${parts[0].padStart(2, '0')}:${parts[1].padStart(2, '0')}:00`;
            } else if (parts.length === 3) {
                reportStep = `${parts[0].padStart(2, '0')}:${parts[1].padStart(2, '0')}:${parts[2].padStart(2, '0')}`;
            }
        } else if (needDefaultGage) {
            reportStep = '01:00:00'; // Default RG1 has 1:00 interval
        }

        L.push('[TITLE]');
        L.push(';;Project Title/Notes');
        L.push(net.title || 'Generated by OpenSWMM 3D Web UI');
        L.push('');

        L.push('[OPTIONS]');
        L.push(';;Option             Value');
        L.push(`FLOW_UNITS           ${units}`);
        L.push(`INFILTRATION         ${opt.infiltration || 'HORTON'}`);
        L.push(`FLOW_ROUTING         ${opt.flowRouting || 'KINWAVE'}`);
        L.push('LINK_OFFSETS         DEPTH');
        L.push('MIN_SLOPE            0');
        L.push('ALLOW_PONDING        NO');
        L.push('SKIP_STEADY_STATE    NO');
        L.push(`START_DATE           ${opt.startDate || '01/01/2026'}`);
        L.push(`START_TIME           ${opt.startTime || '00:00:00'}`);
        L.push(`REPORT_START_DATE    ${opt.startDate || '01/01/2026'}`);
        L.push(`REPORT_START_TIME    ${opt.startTime || '00:00:00'}`);
        L.push(`END_DATE             ${opt.endDate || '01/01/2026'}`);
        L.push(`END_TIME             ${opt.endTime || '12:00:00'}`);
        L.push('SWEEP_START          01/01');
        L.push('SWEEP_END            12/31');
        L.push('DRY_DAYS             0');
        L.push(`REPORT_STEP          ${reportStep}`);
        L.push(`WET_STEP             ${opt.wetStep || '00:05:00'}`);
        L.push(`DRY_STEP             ${opt.dryStep || '01:00:00'}`);
        L.push(`ROUTING_STEP         ${opt.routingStep || '00:00:30'}`);
        L.push('INERTIAL_DAMPING     PARTIAL');
        L.push('NORMAL_FLOW_LIMITED  BOTH');
        L.push('FORCE_MAIN_EQUATION  H-W');
        L.push('VARIABLE_STEP        0.75');
        L.push('LENGTHENING_STEP     0');
        L.push('MIN_SURFAREA         12.557');
        L.push('MAX_TRIALS           8');
        L.push('HEAD_TOLERANCE       0.005');
        L.push('SYS_FLOW_TOL         5');
        L.push('LAT_FLOW_TOL         5');
        L.push('');

        if (!net.rawSections || !net.rawSections['EVAPORATION']) {
            L.push('[EVAPORATION]');
            L.push(';;Data Source    Parameters');
            L.push('CONSTANT         0.0');
            L.push('DRY_ONLY         NO');
            L.push('');
        }

        if (gages.length || needDefaultGage) {
            L.push('[RAINGAGES]');
            L.push(';;Name           Format    Interval SCF      Source');
            if (needDefaultGage) {
                L.push('RG1              INTENSITY 1:00     1.0      TIMESERIES TS1');
            }
            gages.forEach(g => {
                const p = g.props;
                L.push(`${this.pad(g.id, 16)} ${this.pad(p.format, 9)} ${this.pad(p.interval, 8)} ${this.pad(p.scf, 8)} ${p.sourceType} ${p.sourceName}`);
            });
            L.push('');
        }

        // --- Subcatchments ---
        if (net.subcatchments.length) {
            L.push('[SUBCATCHMENTS]');
            L.push(';;Name           Rain Gage        Outlet           Area     %Imperv  Width    %Slope   CurbLen  SnowPack');
            net.subcatchments.forEach(s => {
                const p = s.props;
                L.push(`${this.pad(s.id, 16)} ${this.pad(p.raingage || 'RG1', 16)} ${this.pad(p.outlet || '*', 16)} ${this.pad(p.area, 8)} ${this.pad(p.imperv, 8)} ${this.pad(p.width, 8)} ${this.pad(p.slope, 8)} ${this.pad(p.curbLen || 0, 8)}`);
            });
            L.push('');
            if (!net.rawSections || !net.rawSections['SUBAREAS']) {
                L.push('[SUBAREAS]');
                L.push(';;Subcatchment   N-Imperv   N-Perv     S-Imperv   S-Perv     PctZero    RouteTo');
                net.subcatchments.forEach(s => {
                    L.push(`${this.pad(s.id, 16)} 0.01       0.1        0.05       0.05       25         OUTLET`);
                });
                L.push('');
            }
            if (!net.rawSections || !net.rawSections['INFILTRATION']) {
                L.push('[INFILTRATION]');
                L.push(';;Subcatchment   MaxRate    MinRate    Decay      DryTime    MaxInfil');
                net.subcatchments.forEach(s => {
                    L.push(`${this.pad(s.id, 16)} 3.0        0.5        4          7          0`);
                });
                L.push('');
            }

        }

        // --- Nodes ---
        const junctions = net.nodes.filter(n => n.type === 'JUNCTION');
        const outfalls = net.nodes.filter(n => n.type === 'OUTFALL');
        const storages = net.nodes.filter(n => n.type === 'STORAGE');
        const dividers = net.nodes.filter(n => n.type === 'DIVIDER');

        if (junctions.length) {
            L.push('[JUNCTIONS]');
            L.push(';;Name           Elevation  MaxDepth   InitDepth  SurDepth   Aponded');
            junctions.forEach(n => {
                const p = n.props;
                L.push(`${this.pad(n.id, 16)} ${this.pad(p.invertEl, 10)} ${this.pad(p.maxDepth, 10)} ${this.pad(p.initDepth, 10)} ${this.pad(p.surDepth, 10)} ${this.pad(p.aponded, 10)}`);
            });
            L.push('');
        }

        if (outfalls.length) {
            L.push('[OUTFALLS]');
            L.push(';;Name           Elevation  Type       Stage Data       Gated');
            outfalls.forEach(n => {
                const p = n.props;
                const stage = (p.outfallType === 'FIXED') ? this.pad(p.stageData || 0, 16) : this.pad('', 16);
                L.push(`${this.pad(n.id, 16)} ${this.pad(p.invertEl, 10)} ${this.pad(p.outfallType || 'FREE', 10)} ${stage} ${p.gated || 'NO'}`);
            });
            L.push('');
        }

        if (storages.length) {
            L.push('[STORAGE]');
            L.push(';;Name           Elev.      MaxDepth   InitDepth  Shape      Curve Params                 Fevap    Psi      Ksat     IMD');
            storages.forEach(n => {
                const p = n.props;
                L.push(`${this.pad(n.id, 16)} ${this.pad(p.invertEl, 10)} ${this.pad(p.maxDepth, 10)} ${this.pad(p.initDepth, 10)} ${this.pad(p.shape || 'FUNCTIONAL', 10)} ${this.pad(p.coeff, 8)} ${this.pad(p.exponent, 8)} ${this.pad(p.constant, 8)} 0`);
            });
            L.push('');
        }

        if (dividers.length) {
            L.push('[DIVIDERS]');
            L.push(';;Name           Elevation  Diverted Link    Type       Parameters');
            dividers.forEach(n => {
                const p = n.props;
                L.push(`${this.pad(n.id, 16)} ${this.pad(p.invertEl, 10)} ${this.pad(p.divertedLink || '*', 16)} ${this.pad(p.dividerType || 'CUTOFF', 10)} ${this.pad(p.param, 10)} ${this.pad(p.maxDepth, 10)} 0          0`);
            });
            L.push('');
        }

        // --- Links ---
        const conduits = net.links.filter(l => l.type === 'CONDUIT');
        const pumps = net.links.filter(l => l.type === 'PUMP');
        const weirs = net.links.filter(l => l.type === 'WEIR');
        const orifices = net.links.filter(l => l.type === 'ORIFICE');

        if (conduits.length) {
            L.push('[CONDUITS]');
            L.push(';;Name           From Node        To Node          Length     Roughness  InOffset   OutOffset  InitFlow   MaxFlow');
            conduits.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.length || 100, 10)} ${this.pad(p.roughness, 10)} ${this.pad(p.inOffset, 10)} ${this.pad(p.outOffset, 10)} ${this.pad(p.initFlow, 10)} ${this.pad(p.maxFlow, 10)}`);
            });
            L.push('');
        }

        if (pumps.length) {
            L.push('[PUMPS]');
            L.push(';;Name           From Node        To Node          Pump Curve       Status   Startup  Shutoff');
            pumps.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.pumpCurve || '*', 16)} ${this.pad(p.status || 'ON', 8)} ${this.pad(p.startup, 8)} ${this.pad(p.shutoff, 8)}`);
            });
            L.push('');
        }

        if (weirs.length) {
            L.push('[WEIRS]');
            L.push(';;Name           From Node        To Node          Type         CrestHt    Qcoeff     Gated    EndCon   EndCoeff');
            weirs.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.weirType || 'TRANSVERSE', 12)} ${this.pad(p.crestHt, 10)} ${this.pad(p.qCoeff, 10)} ${this.pad(p.gated || 'NO', 8)} 0        0`);
            });
            L.push('');
        }

        if (orifices.length) {
            L.push('[ORIFICES]');
            L.push(';;Name           From Node        To Node          Type         Offset     Qcoeff     Gated    CloseTime');
            orifices.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(l.from, 16)} ${this.pad(l.to, 16)} ${this.pad(p.orificeType || 'SIDE', 12)} ${this.pad(p.offset, 10)} ${this.pad(p.qCoeff, 10)} ${this.pad(p.gated || 'NO', 8)} 0`);
            });
            L.push('');
        }

        // --- Cross sections (conduits, weirs, orifices) ---
        const xsecLinks = [...conduits, ...weirs, ...orifices];
        if (xsecLinks.length) {
            L.push('[XSECTIONS]');
            L.push(';;Link           Shape        Geom1      Geom2      Geom3      Geom4      Barrels    Culvert');
            xsecLinks.forEach(l => {
                const p = l.props;
                L.push(`${this.pad(l.id, 16)} ${this.pad(p.xShape || 'CIRCULAR', 12)} ${this.pad(p.geom1 ?? 1, 10)} ${this.pad(p.geom2 ?? 0, 10)} ${this.pad(p.geom3 ?? 0, 10)} ${this.pad(p.geom4 ?? 0, 10)} ${this.pad(p.barrels || 1, 10)}`);
            });
            L.push('');
        }

        // --- Default timeseries when any raingage references TS1 ---
        if (!net.rawSections || !net.rawSections['TIMESERIES']) {
            const usesTS1 = needDefaultGage || gages.some(g => g.props.sourceType === 'TIMESERIES' && g.props.sourceName === 'TS1');
            if (usesTS1) {
                L.push('[TIMESERIES]');
                L.push(';;Name           Date       Time       Value');
                L.push('TS1                         0:00       10');
                L.push('TS1                         1:00       20');
                L.push('TS1                         2:00       5');
                L.push('TS1                         3:00       0');
                L.push('');
            }
        }

        L.push('[REPORT]');
        L.push(';;Reporting Options');
        L.push('INPUT      NO');
        L.push('CONTROLS   NO');
        L.push('SUBCATCHMENTS ALL');
        L.push('NODES ALL');
        L.push('LINKS ALL');
        
        if (net.rawSections && net.rawSections['REPORT']) {
            // Include user's other report options if any, but ignore standard ones we just set
            const ignores = ['INPUT', 'CONTROLS', 'SUBCATCHMENTS', 'NODES', 'LINKS'];
            net.rawSections['REPORT'].forEach(line => {
                const upper = line.toUpperCase().trim();
                // ONLY allow valid report parameters, don't allow node IDs/coordinates to leak in!
                if (upper && !ignores.some(ig => upper.startsWith(ig))) {
                    // Quick heuristic: a valid report option typically doesn't start with a node name (which won't match standard SWMM options)
                    if (upper.startsWith('CONTINUOUS') || upper.startsWith('AVERAGES') || upper.startsWith('FLOWSTATS') || upper.startsWith('PARAMETERS')) {
                        L.push(line);
                    }
                }
            });
        }
        L.push('');

        // --- Geometry ---
        L.push('[COORDINATES]');
        L.push(';;Node           X-Coord            Y-Coord');
        net.nodes.filter(n => n.type !== 'RAINGAGE').forEach(n => {
            L.push(`${this.pad(n.id, 16)} ${this.pad(n.lngLat[0].toFixed(6), 18)} ${this.pad(n.lngLat[1].toFixed(6), 18)}`);
        });
        L.push('');

        const withVerts = net.links.filter(l => l.vertices && l.vertices.length);
        if (withVerts.length) {
            L.push('[VERTICES]');
            L.push(';;Link           X-Coord            Y-Coord');
            withVerts.forEach(l => {
                l.vertices.forEach(v => {
                    L.push(`${this.pad(l.id, 16)} ${this.pad(v[0].toFixed(6), 18)} ${this.pad(v[1].toFixed(6), 18)}`);
                });
            });
            L.push('');
        }

        if (net.subcatchments.length) {
            L.push('[POLYGONS]');
            L.push(';;Subcatchment   X-Coord            Y-Coord');
            net.subcatchments.forEach(s => {
                s.ring.forEach(pt => {
                    L.push(`${this.pad(s.id, 16)} ${this.pad(pt[0].toFixed(6), 18)} ${this.pad(pt[1].toFixed(6), 18)}`);
                });
            });
            L.push('');
        }

        if (gages.length) {
            L.push('[SYMBOLS]');
            L.push(';;Gage           X-Coord            Y-Coord');
            gages.forEach(g => {
                L.push(`${this.pad(g.id, 16)} ${this.pad(g.lngLat[0].toFixed(6), 18)} ${this.pad(g.lngLat[1].toFixed(6), 18)}`);
            });
            L.push('');
        }


        // --- Append any raw sections that we did not parse explicitly ---
        if (net.rawSections) {
            const handledSections = new Set([
                'TITLE', 'OPTIONS', 'RAINGAGES', 'SUBCATCHMENTS',
                'JUNCTIONS', 'OUTFALLS', 'STORAGE',
                'DIVIDERS', 'CONDUITS', 'PUMPS', 'WEIRS', 'ORIFICES', 'XSECTIONS',
                'COORDINATES', 'VERTICES', 'POLYGONS', 'SYMBOLS', 'REPORT'
            ]);
            for (const [secName, lines] of Object.entries(net.rawSections)) {
                if (!handledSections.has(secName)) {
                    L.push(`[${secName}]`);
                    lines.forEach(line => L.push(line));
                    L.push('');
                }
            }
        }

        return L.join('\n');
    }

    downloadInp(net) {
        const text = this.generateInp(net);
        const blob = new Blob([text], { type: 'text/plain' });
        const a = document.createElement('a');
        a.href = URL.createObjectURL(blob);
        a.download = (net.title.replace(/\s+/g, '_') || 'network') + '.inp';
        a.click();
        URL.revokeObjectURL(a.href);
        return text;
    }
}

window.inpExporter = new InpExporter();
